#!/bin/bash
# Этот скрипт автоматически выполняется certbot при успешном обновлении сертификатов
# Certbot автоматически запускает все исполняемые скрипты из /etc/letsencrypt/renewal-hooks/deploy/
# при выполнении команды certbot renew (которая запускается автоматически через certbot.timer)
#
# Перезагружаем сервисы для применения новых сертификатов
# Все сервисы загружают сертификаты при старте, поэтому при обновлении файлов
# нужно перезагрузить/перезапустить сервисы, чтобы они загрузили новые сертификаты
systemctl restart asterisk
systemctl restart coturn
systemctl restart intercom-backend
